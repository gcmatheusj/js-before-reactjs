/**
 * Variáveis no JS nada mais são do que nomes que servem para identificarmos
 * algum valor.
 * 
 * Podemos criar variáveis de 3 formas: var, let e const.
 */

let idade = 26

console.log(idade)

idade = 40

console.log(idade)

const nome = 'Matheus'

console.log(nome)

nome = 'Felipe'

console.log(nome)